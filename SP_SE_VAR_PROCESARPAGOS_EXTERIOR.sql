                -- 1. DETECTAR CAMBIO DE GRUPO
                IF (NUMCTAPAGADOR_ANT IS NULL OR NUMCTAPAGADOR_ANT <> CICLOFACTURAS.CTAPAGADOR OR NUMIDBENEFICIARIO_ANT <> CICLOFACTURAS.IDUNICOBENEFICIARIO OR
