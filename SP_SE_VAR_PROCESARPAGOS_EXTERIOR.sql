										WHERE CURRENCYID = CHRICMONEDACUENTABENEFICIARIO AND SUBSIDIARYID = INTSUBSIDIARYID AND CORRESPONDENTBANKTYPESID = INTCORRESPONDENTBANKTYPESID FETCH FIRST 1 ROW ONLY ) ;
